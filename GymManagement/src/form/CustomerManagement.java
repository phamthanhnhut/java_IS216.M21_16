            dcDateJoin.setDate(d_DateJoin);
            java.util.Date d_BirthDay = new SimpleDateFormat("dd/MM/yyyy").parse((String) tableCustomer.getValueAt(item, 4));
            dcBirthday.setDate(d_BirthDay);
