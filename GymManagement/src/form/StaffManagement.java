    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        tableModelStaff = (DefaultTableModel) tableStaff.getModel();

        if (tableStaff.getSelectedRowCount() == 1) {
            int responseDelete = JOptionPane.showConfirmDialog(this, "Bạn có muốn xoá Nhân Viên " + txtName.getText(), "Lựa chọn xoá!", JOptionPane.YES_NO_OPTION);
            if (responseDelete == JOptionPane.YES_OPTION) {
                tableModelStaff.removeRow(tableStaff.getSelectedRow());
                JOptionPane.showMessageDialog(this, "Nhân Viên " + txtName.getText() + " đã xoá thành công!");
            } else {
                JOptionPane.showMessageDialog(this, "Bạn đã huỷ xoá Nhân Viên " + txtName.getText());
            }

        } else {
            if (tableStaff.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Chưa có Nhân Viên nào trong bảng cả!");
            } else {
                JOptionPane.showMessageDialog(this, "Chỉ được chọn 1 Nhân Viên để xoá!");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

