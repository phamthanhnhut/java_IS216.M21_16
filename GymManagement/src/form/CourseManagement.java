    private void btnUpdate1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdate1ActionPerformed
        // TODO add your handling code here:
        try ( Connection conn = ConnectionUtils.getOracleConnection()) {
            Date dStart = dcStart.getDate();
            String sStart = String.format("%1$td-%1$tm-%1$tY", dStart);
            Date dFinish = dcFinish.getDate();
            String sFinish = String.format("%1$td-%1$tm-%1$tY", dFinish);
            String sql_update = "UPDATE course set course_name = '" + txtCourseName.getText() + "', course_type = '" + cbCourse_type.getSelectedItem() + "', course_price = '" + txtPrice.getText() + "', course_capacity = '" + txtCapacity.getText() + "', course_room = '" + txtRoom.getText() + "', course_instructor_id = '" + txtInstructor.getText() + "', start_date = to_date('" + sStart + "', 'dd/MM/yyyy'), end_date = to_date('" + sFinish
                    + "WHERE course_id = '" + id + "';";
            Statement statement = conn.createStatement();
            statement.executeUpdate(sql_update);
            setTableCourse();
            conn.close();
            JOptionPane.showMessageDialog(this, "Sửa thông tin Thiết Bị thành công!");

        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(CourseManagement.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println(ex);
        }
    }//GEN-LAST:event_btnUpdate1ActionPerformed

