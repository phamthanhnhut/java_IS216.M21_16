    private void getRowStaff(int item) {
        try {
            cbStaff_type.setEnabled(true);
            txtSalary.setEnabled(true);

            id = (String) tableStaff.getValueAt(item, 0);
            txtName.setText((String) tableStaff.getValueAt(item, 1));
            txtTelephone.setText((String) tableStaff.getValueAt(item, 5));
            txtAddress.setText((String) tableStaff.getValueAt(item, 6));
            txtSalary.setText((String) tableStaff.getValueAt(item, 8));
            Date d_DateJoin = new SimpleDateFormat("dd-mm-yyyy").parse((String) tableStaff.getValueAt(item, 3));
            dcDateJoin.setDate(d_DateJoin);
            Date d_BirthDay = new SimpleDateFormat("dd-mm-yyyy").parse((String) tableStaff.getValueAt(item, 4));
            dcBirthday.setDate(d_BirthDay);
            cbGender.setSelectedItem(tableStaff.getValueAt(item, 2));
            cbStaff_type.setSelectedItem(tableStaff.getValueAt(item, 7));
            txtTimeJoined.setText(String.valueOf(tableStaff.getValueAt(item, 9)));
        } catch (ParseException ex) {
            Logger.getLogger(StaffManagement.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println(ex);
        }
    }

    private void setTableStaff() {
        try ( Connection conn = ConnectionUtils.getOracleConnection()) {

            String sql_select = "SELECT * FROM staff ORDER BY staff_id;";

            Statement statement = conn.createStatement();
            ResultSet rs = statement.executeQuery(sql_select);
            tableModelStaff = (DefaultTableModel) tableStaff.getModel();
            tableModelStaff.setRowCount(0);
            while (rs.next()) {
                String sID = rs.getString(1);
                String sName = rs.getString(2);
                String sGender = rs.getString(3);
                java.util.Date dDateJoin = rs.getDate(4);
                String sDateJoin = new SimpleDateFormat("dd-mm-yyyy").format(dDateJoin);
                java.util.Date dBirthday = rs.getDate(5);
                String sBirthday = new SimpleDateFormat("dd-mm-yyyy").format(dBirthday);
                String sTelephone = rs.getString(6);
                String sAddress = rs.getString(7);
                String sType = rs.getString(8);
                String sSalary = rs.getString(9);
                String sJoined = rs.getString(10);
                tableModelStaff.addRow(new Object[]{sID, sName, sGender, sDateJoin, sBirthday, sTelephone, sAddress, sType, sSalary, sJoined});
            }
            conn.close();

        } catch (SQLException | ClassNotFoundException | NumberFormatException ex) {
            Logger.getLogger(StaffManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

