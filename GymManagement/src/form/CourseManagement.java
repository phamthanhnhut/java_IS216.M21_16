    private void getRowCourse(int item) {
        try {
            cbCourse_type.setEnabled(true);
            txtPrice.setEnabled(true);

            id = (String) tableCourse.getValueAt(item, 0);
            txtCourseName.setText((String) tableCourse.getValueAt(item, 1));
            txtCapacity.setText((String) tableCourse.getValueAt(item, 4));
            txtRoom.setText((String) tableCourse.getValueAt(item, 5));
            txtInstructor.setText((String) tableCourse.getValueAt(item, 6));
            txtPrice.setText((String) tableCourse.getValueAt(item, 3));
            Date d_Start = new SimpleDateFormat("dd/MM/yyyy").parse((String) tableCourse.getValueAt(item, 7));
            dcStart.setDate(d_Start);
            Date d_Finish = new SimpleDateFormat("dd/MM/yyyy").parse((String) tableCourse.getValueAt(item, 8));
            dcFinish.setDate(d_Finish);
            cbCourse_type.setSelectedItem(tableCourse.getValueAt(item, 2));

        } catch (ParseException ex) {
            Logger.getLogger(CourseManagement.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println(ex);
        }
    }

    private void setTableCourse() {
        try ( Connection conn = ConnectionUtils.getOracleConnection()) {

            String sql_select = "SELECT * FROM course"
                    + "WHERE course.instructor_id = staff.staff_id AND room.room_id = course.course_room AND staff_type = 'Huấn luyện viên'"
                    + "ORDER BY course_id;";

            Statement statement = conn.createStatement();
            ResultSet rs = statement.executeQuery(sql_select);
            tableModelCourse = (DefaultTableModel) tableCourse.getModel();
            tableModelCourse.setRowCount(0);
            while (rs.next()) {
                String sID = rs.getString(1);
                String sName = rs.getString(2);
                String sType = rs.getString(3);
                String sPrice = rs.getString(4);
                Date dStart = rs.getDate(8);
                String sStart = new SimpleDateFormat("dd/MM/yyyy").format(dStart);
                Date dFinish = rs.getDate(9);
                String sFinish = new SimpleDateFormat("dd/MM/yyyy").format(dFinish);
                String sCapacity = rs.getString(5);
                String sRoom = rs.getString(6);
                String sInstructor = rs.getString(7);
                tableModelCourse.addRow(new Object[]{sID, sName, sType, sPrice, sCapacity, sRoom, sInstructor, sStart, sFinish});
            }
            conn.close();

        } catch (SQLException | ClassNotFoundException | NumberFormatException ex) {
            Logger.getLogger(CustomerManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

