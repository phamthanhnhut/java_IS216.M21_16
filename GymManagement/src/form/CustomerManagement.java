    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try ( Connection conn = ConnectionUtils.getOracleConnection()) {
            java.util.Date dBirthday = dcBirthday.getDate();
            String sBirthday = String.format("%1$td-%1$tm-%1$tY", dBirthday);
            java.util.Date dDatejoin = dcDateJoin.getDate();
            String sDatejoin = String.format("%1$td-%1$tm-%1$tY", dDatejoin);
            String sql_update = "UPDATE customer set cus_name = '" + txtName.getText() + "', cus_gender = '" + cbGender.getSelectedItem() + "', cus_date_join = to_date('" + sDatejoin + "', 'dd/MM/yyyy'), cus_birthday = to_date('" + sBirthday + "', 'dd/MM/yyyy'), cus_phone_number = '" + txtTelephone.getText() + "', cus_address = '" + txtAddress.getText()
                    + "' WHERE cus_id = '" + id + "';";
            Statement statement = conn.createStatement();
            statement.executeUpdate(sql_update);
            setTableCustomer();
            conn.close();
            JOptionPane.showMessageDialog(this, "Sửa thông tin Khách Hàng thành công!");

        } catch (SQLException ex) {
            Logger.getLogger(CustomerManagement.class.getName()).log(Level.SEVERE, null, ex);
            if (ex.getErrorCode() >= 20000) {
                JOptionPane.showMessageDialog(this, "Ngày sinh của khách hàng phải nhỏ hơn ngày đăng ký thành viên!", "Lỗi sửa ngày sinh & ngày đăng ký!", JOptionPane.ERROR_MESSAGE);
            }
            System.out.println(ex);

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CustomerManagement.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println(ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

