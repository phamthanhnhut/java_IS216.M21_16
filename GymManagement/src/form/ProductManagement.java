    private void getRowProduct(int item) {
        cbProduct_type.setEnabled(true);
        txtCost.setEnabled(true);
        id = (String) tableProduct.getValueAt(item, 0);
        txtProductName.setText((String) tableProduct.getValueAt(item, 1));
        txtCost.setText((String) tableProduct.getValueAt(item, 4));
        txtNumber.setText((String) tableProduct.getValueAt(item, 6));
        cbConditionDevice.setSelectedItem(tableProduct.getValueAt(item, 5));
        cbConditionFoods.setSelectedItem(tableProduct.getValueAt(item, 5));
        cbProduct_type.setSelectedItem(tableProduct.getValueAt(item, 2));
    }
    
    private void setTableProduct() {
        try ( Connection conn = ConnectionUtils.getOracleConnection()) {
            
            String sql_select = "SELECT * FROM product;";
            
            Statement statement = conn.createStatement();
            ResultSet rs = statement.executeQuery(sql_select);
            tableModelProduct = (DefaultTableModel) tableProduct.getModel();
            tableModelProduct.setRowCount(0);
            while (rs.next()) {
                String sID = rs.getString(1);
                String sName = rs.getString(2);
                String sType = rs.getString(3);
                String sPrice = rs.getString(4);
                String sNumber = rs.getString(5);
                String sCondition = rs.getString(6);
                tableModelProduct.addRow(new Object[]{sID, sName, sType, sPrice, sNumber, sCondition});
            }
            conn.close();
            
        } catch (SQLException | ClassNotFoundException | NumberFormatException ex) {
            Logger.getLogger(CustomerManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

