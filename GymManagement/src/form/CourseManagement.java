    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        tableModelCourse = (DefaultTableModel) tableCourse.getModel();

        if (tableCourse.getSelectedRowCount() == 1) {
            int responseDelete = JOptionPane.showConfirmDialog(this, "Bạn có muốn xoá thông tin Khoá Học " + txtName.getText(), "Lựa chọn xoá!", JOptionPane.YES_NO_OPTION);
            if (responseDelete == JOptionPane.YES_OPTION) {
                tableModelCourse.removeRow(tableCourse.getSelectedRow());
                JOptionPane.showMessageDialog(this, "Khoá học " + txtName.getText() + " đã xoá thành công!");
            } else {
                JOptionPane.showMessageDialog(this, "Bạn đã huỷ xoá Khoá học " + txtName.getText());
            }

        } else {
            if (tableCourse.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Chưa có Khoá Học nào trong bảng cả!");
            } else {
                JOptionPane.showMessageDialog(this, "Chỉ được chọn lần lượt 1 Khoá Học để xoá!");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

