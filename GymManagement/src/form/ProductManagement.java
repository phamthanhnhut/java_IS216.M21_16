    private void cbProduct_typeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbProduct_typeActionPerformed
        // TODO add your handling code here:
        int selectedType = cbProduct_type.getSelectedIndex();
        if (selectedType == 0) {
            cbConditionDevice.setEnabled(true);
            cbConditionFoods.setEnabled(false);
            cbConditionFoods.setSelectedItem(null);
        } else if (selectedType == 1) {
            cbConditionDevice.setEnabled(false);
            cbConditionFoods.setEnabled(true);
            cbConditionDevice.setSelectedItem(null);
        }
    }//GEN-LAST:event_cbProduct_typeActionPerformed
    
    private void resetProduct() {
        txtProductName.setText("");
        txtCost.setText("");
        txtNumber.setText("");
        txtSearch.setText("");
        cbConditionDevice.setSelectedItem(null);
        cbConditionFoods.setSelectedItem(null);
        cbProduct_type.setSelectedItem(null);
    }

